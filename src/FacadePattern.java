public class FacadePattern {
    //   为系统中的一组接口提供一个一致的界面，Facade模式定义了一个高层接口，这个接口使得这一子系统更加容易使用。
    //  优点
    //   外观模式可以让一个系统更加容易使用
    //
    //  我们提供给用户的某个功能，可能是包含很多个步骤的，
    //   但我们可以把这些步骤封装到一个统一的接口中，让用户感觉仅仅就是一个单一的操作，使用起来也就更加简单。
    //  比如说，我们往回一些年，那时候的相机不是每个人都会用的，
    //   按下快门前还需要调焦等操作，然而现在呢，我们拍照只需要按下快门就行了，
    //   中间的一系列操作都被自动执行了。外观模式也就是提供这样一种机制。
}